def handle_sigterm(*args):
    """
    Handle forceful container termination gracefully.
    
    Parameters
    ----------
    *args : tuple
        Variable length argument list passed by the signal handler.
    """
    print("Received SIGTERM, shutting down gracefully...")
    for item in os.listdir("/tmp"):
        if item.startswith("run_"):
            shutil.rmtree(os.path.join("/tmp", item), ignore_errors=True)
    os._exit(0)

def download_gcs_uri(client: storage.Client, uri: str, destination: str):
    """
    Downloads a single GCS file to a local destination.
    
    Parameters
    ----------
    client : storage.Client
        The instantiated Google Cloud Storage client.
    uri : str
        The full gs:// URI of the file to download.
    destination : str
        The local file path where the file should be saved.
    """
    bucket_name = uri.replace("gs://", "").split("/")[0]
    blob_path = "/".join(uri.replace("gs://", "").split("/")[1:])
    bucket = client.bucket(bucket_name)
    blob = bucket.blob(blob_path)
    blob.download_to_filename(destination)

def upload_gcs_uri(client: storage.Client, local_path: str, uri: str, content_type="application/json"):
    """
    Uploads a local file to a GCS destination.
    
    Parameters
    ----------
    client : storage.Client
        The instantiated Google Cloud Storage client.
    local_path : str
        The local file path of the file to upload.
    uri : str
        The full gs:// URI destination.
    content_type : str, optional
        The MIME type of the file being uploaded (default is "application/json").
    """
    bucket_name = uri.replace("gs://", "").split("/")[0]
    blob_path = "/".join(uri.replace("gs://", "").split("/")[1:])
    bucket = client.bucket(bucket_name)
    blob = bucket.blob(blob_path)
    blob.upload_from_filename(local_path, content_type=content_type)

